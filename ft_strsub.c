#include "libft.h"

char	*ft_strsub(const char *s, unsigned int start, size_t len)
{
	char			*returnable;
	unsigned int	i;

	returnable = (char*)malloc(sizeof(char) * len + 1);
	i = 0;
	if (returnable)
	{
		while (i < len)
		{
			returnable[i] = s[start + i];
			i++;
		}
		returnable[i] = '\0';
		return (returnable);
	}
	return (NULL);
}
